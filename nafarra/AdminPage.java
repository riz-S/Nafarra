    private void makeOrderButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_makeOrderButtonActionPerformed
        // TODO add your handling code here:
        try {
            connection.setAutoCommit(false);
            String jnsKirim = delivType.getSelectedItem().equals("REGULER")? "REG":"EXP";
            String query = "exec addOrder '" + stName.getText() + "','" + stNum.getText() + "','" 
                    + zipCode.getText() + "','" + city.getText() + "','" + java.time.LocalDate.now() + "','" + custPhone.getText() 
                    + "','" + jnsKirim + "','" + prov.getText() + "'";
            Statement statement = connection.createStatement();
            int input = JOptionPane.showConfirmDialog(null, 
                "Apakah anda yakin ingin membuat order baru?", "Select an Option...",JOptionPane.OK_CANCEL_OPTION);
            statement.executeUpdate(query);
            if(input == 0){
                connection.commit();
                JOptionPane.showMessageDialog(this, "Order berhasil dibuat!");
            }
            else{
                connection.rollback();
                JOptionPane.showMessageDialog(this, "Order gagal dibuat!","Error...",
                JOptionPane.ERROR_MESSAGE);
            }
            stName.setText(""); stNum.setText(""); zipCode.setText(""); city.setText(""); custPhone.setText(""); prov.setText("");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Order gagal dibuat!","Error...",
                    JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_makeOrderButtonActionPerformed

