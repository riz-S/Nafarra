    public void populateJTable(String query, javax.swing.JTable table) {
        try {
            Statement statement = connection.createStatement();
            statement.executeQuery(query);
            ResultSet rs = statement.executeQuery(query);
            ResultSetMetaData mtdt = rs.getMetaData();
            DefaultTableModel tm = (DefaultTableModel)table.getModel();

            Vector<String> columnNames = new Vector<String>();
            int columnCount = mtdt.getColumnCount();
            for (int i = 1; i <= columnCount; i++) {
                columnNames.add(mtdt.getColumnName(i));
            }

            Vector<Vector<Object>> data = new Vector<Vector<Object>>();
            while (rs.next()) {
                Vector<Object> vector = new Vector<Object>();
                for (int i = 1; i <= columnCount; i++) {
                    vector.add(rs.getObject(i));
                }
                data.add(vector);
            }
            tm.setDataVector(data, columnNames);
        } catch (SQLException throwables) {
            throwables.printStackTrace();
        }
    }
    
